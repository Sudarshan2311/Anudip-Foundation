import math
x = 15
y = 7

sqroot = math.sqrt(x)
power = math.pow(x, y)
exponential = math.exp(x)
log = math.log(x)
base_10_log = math.log10(x)

sin_x = math.sin(math.pi / 8)
cos_x = math.cos(math.pi / 5)
tan_x = math.tan(math.pi / 9)


ceil_result = math.ceil(y)
floor_result = math.floor(y)
print("Square root:", sqroot)
print("Power:", power)
print("Exponential:", exponential)
print("Natural Logarithm:", log)
print("Base-10 Logarithm:", base_10_log)
print("Sine:", sin_x)
print("Cosine:", cos_x)
print("Tangent:", tan_x)
print("Ceiling:", ceil_result)
print("Floor:", floor_result)




